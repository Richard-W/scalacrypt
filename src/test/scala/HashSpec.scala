package xyz.wiedenhoeft.scalacrypt

import org.scalatest._
import hash._

class HashSpec extends FlatSpec with Matchers {

  "SHA1" should "conform to the testvectors" in {
    val vectors = Seq(
      (
        "".getBytes.toSeq,
        Seq(0xda, 0x39, 0xa3, 0xee, 0x5e, 0x6b, 0x4b, 0x0d, 0x32, 0x55, 0xbf, 0xef, 0x95, 0x60, 0x18, 0x90, 0xaf, 0xd8, 0x07, 0x09)
      ), (
        "abc".getBytes.toSeq,
        Seq(0xa9, 0x99, 0x3e, 0x36, 0x47, 0x06, 0x81, 0x6a, 0xba, 0x3e, 0x25, 0x71, 0x78, 0x50, 0xc2, 0x6c, 0x9c, 0xd0, 0xd8, 0x9d)
      ), (
        "abcdefghbcdefghicdefghijdefghijkefghijklfghijklmghijklmnhijklmnoijklmnopjklmnopqklmnopqrlmnopqrsmnopqrstnopqrstu".getBytes.toSeq,
        Seq(0xa4, 0x9b, 0x24, 0x46, 0xa0, 0x2c, 0x64, 0x5b, 0xf4, 0x19, 0xf9, 0x95, 0xb6, 0x70, 0x91, 0x25, 0x3a, 0x04, 0xa2, 0x59)
      ), (
        Seq.fill[Byte](1000000) { 'a'.toByte },
        Seq(0x34, 0xaa, 0x97, 0x3c, 0xd4, 0xc4, 0xda, 0xa4, 0xf6, 0x1e, 0xeb, 0x2b, 0xdb, 0xad, 0x27, 0x31, 0x65, 0x34, 0x01, 0x6f)
      )
    )

    for (vector <- vectors) {
      val hash = SHA1(vector._1)
      val expected = vector._2 map { _.toByte }
      hash should be(expected)
    }
  }

  "SHA256" should "conform to the testvectors" in {
    val vectors = Seq(
      (
        "".getBytes.toSeq,
        Seq(0xe3, 0xb0, 0xc4, 0x42, 0x98, 0xfc, 0x1c, 0x14, 0x9a, 0xfb, 0xf4, 0xc8, 0x99, 0x6f, 0xb9, 0x24, 0x27, 0xae, 0x41, 0xe4, 0x64, 0x9b, 0x93, 0x4c, 0xa4, 0x95, 0x99, 0x1b, 0x78, 0x52, 0xb8, 0x55)
      ), (
        "abc".getBytes.toSeq,
        Seq(0xba, 0x78, 0x16, 0xbf, 0x8f, 0x01, 0xcf, 0xea, 0x41, 0x41, 0x40, 0xde, 0x5d, 0xae, 0x22, 0x23, 0xb0, 0x03, 0x61, 0xa3, 0x96, 0x17, 0x7a, 0x9c, 0xb4, 0x10, 0xff, 0x61, 0xf2, 0x00, 0x15, 0xad)
      ), (
        "abcdefghbcdefghicdefghijdefghijkefghijklfghijklmghijklmnhijklmnoijklmnopjklmnopqklmnopqrlmnopqrsmnopqrstnopqrstu".getBytes.toSeq,
        Seq(0xcf, 0x5b, 0x16, 0xa7, 0x78, 0xaf, 0x83, 0x80, 0x03, 0x6c, 0xe5, 0x9e, 0x7b, 0x04, 0x92, 0x37, 0x0b, 0x24, 0x9b, 0x11, 0xe8, 0xf0, 0x7a, 0x51, 0xaf, 0xac, 0x45, 0x03, 0x7a, 0xfe, 0xe9, 0xd1)
      ), (
        Seq.fill[Byte](1000000) { 'a'.toByte },
        Seq(0xcd, 0xc7, 0x6e, 0x5c, 0x99, 0x14, 0xfb, 0x92, 0x81, 0xa1, 0xc7, 0xe2, 0x84, 0xd7, 0x3e, 0x67, 0xf1, 0x80, 0x9a, 0x48, 0xa4, 0x97, 0x20, 0x0e, 0x04, 0x6d, 0x39, 0xcc, 0xc7, 0x11, 0x2c, 0xd0)
      )
    )

    for (vector <- vectors) {
      val hash = SHA256(vector._1)
      val expected = vector._2 map { _.toByte }
      hash should be(expected)
    }
  }

  "The hashes" should "conform to the testvectors" in {
    val testbytes = "The quick brown fox jumps over the lazy dog".getBytes.toSeq
    val md5 = MD5(testbytes)
    val sha1 = SHA1(testbytes)
    val sha256 = SHA256(testbytes)

    md5 should be(Seq(0x9e, 0x10, 0x7d, 0x9d, 0x37, 0x2b, 0xb6, 0x82, 0x6b, 0xd8, 0x1d, 0x35, 0x42, 0xa4, 0x19, 0xd6) map { _.toByte })
    sha1 should be(Seq(0x2f, 0xd4, 0xe1, 0xc6, 0x7a, 0x2d, 0x28, 0xfc, 0xed, 0x84, 0x9e, 0xe1, 0xbb, 0x76, 0xe7, 0x39, 0x1b, 0x93, 0xeb, 0x12) map { _.toByte })
    sha256 should be(Seq(0xd7, 0xa8, 0xfb, 0xb3, 0x07, 0xd7, 0x80, 0x94, 0x69, 0xca, 0x9a, 0xbc, 0xb0, 0x08, 0x2e, 0x4f, 0x8d, 0x56, 0x51, 0xe4, 0x6d, 0x3c, 0xdb, 0x76, 0x2d, 0x02, 0xd0, 0xbf, 0x37, 0xc9, 0xe5, 0x92) map { _.toByte })
  }

  "The hash lengths" should "be right." in {
    MD5.length should be(16)
    SHA1.length should be(20)
    SHA256.length should be(32)
  }

  "Hashes" should "be able to process an iterator returning a future." in {
    val testbytes = "The quick brown fox jumps over the lazy dog".getBytes.toIterator.grouped(5)

    val (iterator, futureHash) = SHA256(testbytes)
    // Empty the iterator so the promise gets completed
    while (iterator.hasNext) iterator.next

    futureHash.isCompleted should be(true)
    futureHash.value.get.get should be(Seq(0xd7, 0xa8, 0xfb, 0xb3, 0x07, 0xd7, 0x80, 0x94, 0x69, 0xca, 0x9a, 0xbc, 0xb0, 0x08, 0x2e, 0x4f, 0x8d, 0x56, 0x51, 0xe4, 0x6d, 0x3c, 0xdb, 0x76, 0x2d, 0x02, 0xd0, 0xbf, 0x37, 0xc9, 0xe5, 0x92) map { _.toByte })
  }
}
